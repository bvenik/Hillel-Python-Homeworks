import math


def factorial(a: int) -> int:
    """
    Повертає факторіал числа
    :param a: число
    :return: факторіал числа a
    """
    return math.factorial(a)


def gcd(a: int, b: int) -> int:
    """
    Повертає НСД чисел a і b
    :param a: Число a
    :param b: Число b
    :return: НСК чисел a і b
    """
    return math.gcd(a, b)
