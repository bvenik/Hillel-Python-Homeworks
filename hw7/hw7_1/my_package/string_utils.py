def uppercase(a: str) -> str:
    """
    Переписує рядок a в верхньому регістрі
    :param a: рядок
    :return: рядок a в верхньому регістрі
    """
    return a.upper()


def strip(a: str) -> str:
    """
    Переписує рядок a без пробілів з початку та з кінця
    :param a: рядок
    :return: рядок a без пробілів з початку та з кінця
    """
    return a.strip()
